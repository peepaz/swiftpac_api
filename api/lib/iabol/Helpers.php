function objectToArray($d) {
    if (is_object($d)) {
      // Gets the properties of the given object
      // with get_object_vars function
      $d = get_object_vars($d);
    }
    if (is_array($d)) {
      /*
      * Return array converted to object
      * Using __FUNCTION__ (Magic constant)
      * for recursive call
      */
      return array_filter(array_map(__FUNCTION__, $d), function($a){
          return !is_null($a);
      });
    }elseif (is_bool($d)) {
        if ($d)
            return 1;
        else
            return 0;
    }else{
        // Return array
        return $d;
    }
}

